function createNightVariant(seed: ScenarioSeed): ScenarioSeed {
  const observedAt = seed.sunset
    ? new Date(new Date(seed.sunset).getTime() + 3 * 60 * 60 * 1000).toISOString()
    : new Date(new Date(seed.observedAt).getTime() + 12 * 60 * 60 * 1000).toISOString();

  return {
    ...seed,
    id: `${seed.id}-night`,
    name: `${seed.name} at night`,
    observedAt,
    temperatureF: seed.temperatureF - 8,
    apparentTemperatureF: seed.apparentTemperatureF - 10,
    isDay: false,
    cloudCover: seed.cloudCover,
    dailyRangeF: [seed.dailyRangeF[0] - 5, seed.dailyRangeF[1] - 4],
  };
}
